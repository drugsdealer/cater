const TITLES: Record<Exclude<LegalDoc, null>, string> = {
  privacy: 'Политика в отношении обработки персональных данных',
  consent: 'Согласие на обработку персональных данных',
}

export default function LegalModal({
  doc,
  onClose,
}: {
  doc: LegalDoc
  onClose: () => void
}) {
  useEffect(() => {
    if (!doc) return
    const onKey = (e: KeyboardEvent) => e.key === 'Escape' && onClose()
    document.addEventListener('keydown', onKey)
    document.body.style.overflow = 'hidden'
    return () => {
      document.removeEventListener('keydown', onKey)
      document.body.style.overflow = ''
    }
  }, [doc, onClose])

  if (!doc) return null

  return (
    <div className="modal" role="dialog" aria-modal="true" aria-label={TITLES[doc]}>
      <div className="modal__overlay" onClick={onClose} />
      <div className="modal__card">
        <div className="modal__head">
          <h3 className="modal__title">{TITLES[doc]}</h3>
          <button className="modal__close" onClick={onClose} aria-label="Закрыть">
            ✕
          </button>
        </div>
        <div className="modal__body">
          {doc === 'privacy' ? <PrivacyText /> : <ConsentText />}
        </div>
      </div>
    </div>
  )
}

